//Рекурсивная функция поиска заданного покрывающего дерева.
//Входные данные:
//   неизменяемые:
//		root_first - указатель на первое дерево, для которого нужно найти покрытие;
//		root_second - указатель на второе дерево, для которого нужно найти покрытие;
//Результат - указатель на корень покрывающего дерева.
